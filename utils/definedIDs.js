/**
 * Convert children object to a map keyed by id (pageId)
 * This is useful for dashboard pages to look up card configurations by pageId
 * @param {Object} children - Children object from servicesMenu or mastersMenu
 * @returns {Object} Map keyed by id (pageId)
 */
export const getCardConfigById = (children) => {
    const configMap = {}
    Object.values(children).forEach(child => {
        if (child.id) {
            configMap[child.id] = child
        }
    })
    return configMap
}

